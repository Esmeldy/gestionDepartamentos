    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        // Eliminar Registro
        if (!txtCodigo.getText().isEmpty()){
            if (JOptionPane.showConfirmDialog(rootPane, "¿Estás seguro?", "Eliminar registro", JOptionPane.YES_NO_OPTION)
                    == JOptionPane.YES_NO_OPTION)
            {
                txtZona.setText(dP.Borrar(Integer.parseInt(txtCodigo.getText())));

                txtAlerta.setText("");
                txtAlerta.setForeground(new Color(242, 251, 232));
                ResetearCampos();
            }
        }
        else {
            txtAlerta.setText("* El campo código está vacío");
            txtAlerta.setForeground(Color.red);
            
        }
    }//GEN-LAST:event_EliminarActionPerformed

