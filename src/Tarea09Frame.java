    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        // Buscar

        String c = txtCodigo.getText();
        String n = txtNombre.getText();
        String iL = txtIdLoc.getText();
        String iM = txtIdMan.getText();
        
        /**
         * Controlando que no se incluya dos o mas campos en
         * la busqueda
         */
        if ( (c.trim().length() != 0 && n.trim().length() != 0) 
                || (iL.trim().length() != 0 && iM.trim().length() != 0)
                || (iM.trim().length() != 0 && n.trim().length() != 0)
                || (c.trim().length() != 0 && iL.trim().length() != 0)
                || (c.trim().length() != 0 && iM.trim().length() != 0)
                || (n.trim().length() != 0 && iL.trim().length() != 0))
        {
            txtAlerta.setText("* La busqueda se puede hacer por campo individual");
            txtAlerta.setForeground(Color.red);
        }
        else {
            txtZona.setText(dP.Buscar(c, n, iL, iM));
        }
        
        
        
    }//GEN-LAST:event_BuscarActionPerformed

    private void InsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertarActionPerformed
        //insertar
        
        
        if (!txtCodigo.getText().isEmpty() &&
            !txtNombre.getText().isEmpty() &&
            !txtIdLoc.getText().isEmpty() && 
            !txtIdMan.getText().isEmpty()){
            try {
                txtZona.setText(dP.Insertar(Integer.parseInt(txtCodigo.getText()), txtNombre.getText(),
                        Integer.parseInt(txtIdLoc.getText()), Integer.parseInt(txtIdMan.getText())));
            } catch (SQLException ex) {
                Logger.getLogger(Tarea09Frame.class.getName()).log(Level.SEVERE, null, ex);
            }
             ResetearCampos();
        }
        else{
            txtAlerta.setText("* Hay campos sin rrellenar");
            txtAlerta.setForeground(Color.red);
        }
    }//GEN-LAST:event_InsertarActionPerformed

